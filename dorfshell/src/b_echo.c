#include "ft_minishell.h"

int		builtin_echo(t_mini *m)
{
	int		i;

	if (m->c[1])
	{
		i = 0;
		while (m->c[++i])
		{
			ft_putstr(m->c[i]);
			if (m->c[i + 1])
				write(1, " ", 1);
		}
	}
	write(1, "\n", 1);
	return (1);
}
