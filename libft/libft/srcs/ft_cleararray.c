#include "libft.h"

void		ft_cleararray(char **s)
{
	int		i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != NULL)
	{
		free(s[i]);
		s[i] = NULL;
		i++;
	}
	free(s);
}
